  <section class="greybackground">
    <h1>Interview: Oskar Maarleveld</h1>
    <section class="lineintro"></section>
    <p class="description">Door: Charley Muhren</p>
    <p class="description">26 maart 2019</p>
  </section>

  <img src="/portret.webp" alt="portret" />

  <section>
    <p class="boldintro">
      Interview met docent tekenen en vakdidactiek Oskar Maarleveld,
      seniordocent vakdidactiek aan de Breitner academie en docent tekenen, ckv
      en theater aan het Fons Vitae Lyceum in Amsterdam.
    </p>

    <p>
      Ik heb drie jaar samen met Oskar op het Fons Vitae les gegeven en gezien
      hoe hij (ook in eerste instantie ongemotiveerde) leerlingen aan het
      tekenen kreeg. Ik vertelde hem over mijn onderzoek en vroeg hem waarom
      tekenen volgens hem een goede manier is om te communiceren:
    </p>

    <p class="boldintro">
      “Het is een misvatting dat tekenen een vaardigheid is waar je goed of
      slecht in bent. Sommige vaardigheden vragen natuurlijk om oefening zoals
      heel goed naar de waarneming kunnen tekenen, maar dat is echt iets anders
      dan wanneer je tekent om een boodschap over te brengen of als manier om
      jezelf uit te drukken. Om dat laatste te kunnen doen, moet je je vrij
      voelen om je eigen beelden te vinden en niet te veel bezig zijn met of het
      wel klopt. Ik ben zelf bijvoorbeeld helemaal niet goed in Pictionary omdat
      ik wil dat het een mooie tekening of een handige visuele oplossing wordt.
      Kleine kinderen zijn juist vaak heel goed in communiceren met beeld omdat
      ze nog vrij zijn en niet aan verwachtingen proberen te voldoen. Dat levert
      dan misschien niet altijd een interessante tekening op, maar dat is dan
      ook niet de hoofdzaak. In mijn beeldende lessen laat ik leerlingen vaak
      vrij ‘droedelen’ of tekenen. Ik wil ze hiermee laten inzien dat ze het
      puur als manier om iets te onderzoeken kunnen zien in plaats van als een
      ‘af’ product. Als manier om iets voor jezelf zichtbaar te maken en het
      vervolgens meteen als onderwerp van gesprek te kunnen gebruiken.”
    </p>

    <p class="boldintro">
      “Tekenen is heel direct terwijl er bij bijvoorbeeld fotografie nog een
      stap tussen zit. Als je met je telefoon een foto maakt, kan je nu ook heel
      gemakkelijk een filter over de foto heen zetten. Het resultaat is dan al
      ‘af’ en vaak heel algemeen terwijl tekenen specifiek, uniek en onaf kan
      zijn. Ook toon je door te tekenen je handschrift en dat is voor iedereen
      heel persoonlijk. Ik geloof ook dat als kinderen en volwassenen het
      tekenen als hardop denken zien, het echt nieuwe inzichten kan opleveren.
      Tekenen helpt bijvoorbeeld vaak in vergaderingen met collega’s al om
      helder en concreet te krijgen wat mensen voor zich zien. Taal lijkt al
      snel de lading te dekken maar heel vaak is dat niet het geval.”
    </p>

    <p>
      Ik vind het zelf nog moeilijk om mensen die niet speciaal goed zijn in
      tekenen toch (hun associaties en herinneringen) te laten tekenen. De dag
      na het interview zal ik een workshop geven aan een groep mensen. Tijdens
      deze workshop gaan zij hun herinneringen aan een bruidspaar tekenen. Het
      eindresultaat wil ik vervolgens in een boekje samenbundelen en aan het
      bruidspaar aanbieden. Voor deze workshop heb ik materiaal ontwikkeld en ik
      ben benieuwd wat Oskar daarvan vindt:
    </p>

    <p class="boldintro">
      “Wat ik heel sterk vind aan jouw materiaal is dat het er aantrekkelijk
      uitziet. Het is heel verzorgd en op mooi papier gedrukt. Als je iets op
      een verzorgde manier brengt en aandacht geeft, zie je dat altijd terug in
      de manier waarop men ermee aan de slag gaat. Ik vind het ook interessant
      dat je meerdere aanbiedt.
    </p>

    <p class="boldintro">
      “In de Volkskrant heb je de rubriek ‘Aanzet tot droedelen’. Dat werkt
      ontzettend goed omdat er een voorzet gegeven wordt dus men hoeft niet met
      een blanco vel te beginnen. Tegelijkertijd is de aanzet zo abstract dat
      het nog van alles zou kunnen worden. Dat doet Studio Moniker met
      ‘Conditional design’ ook heel goed. Er worden regels opgelegd waarbinnen
      je je vrijheid kan opzoeken.”
    </p>

    <p class="boldintro">
      “Mensen willen in eerste instantie altijd voldoen aan de verwachtingen en
      op het moment dat ze ervaren dat het juist de bedoeling is dat ze er op
      hun eigen manier mee omgaan, ontstaat er creativiteit. Ik geloof dat
      uiteindelijk iedereen gemotiveerd kan zijn om te tekenen zolang het maar
      duidelijk is dat het er niet om gaat hoe goed of mooi de tekening wordt.
      Het gaat erom dat ze met hun tekening iets duidelijk kunnen maken.”
    </p>

    <p>
      Toen ik zelf tekenles gaf, viel het me op dat de meeste mensen pas blij
      zijn met een tekening als die op de werkelijkheid lijkt. Het kan lastig
      zijn om mensen trots te laten zijn op dat wat ze gemaakt hebben. Ik vroeg
      Oskar hoe hij daar in zijn lessen mee omgaat:
    </p>

    <p class="boldintro">
      “Het is volgens mij dus heel goed om beperkingen op te leggen, zoals onder
      andere Conditonal design doet. Je bent dan al sneller trots omdat je het
      voor elkaar gekregen hebt die beperkingen te omzeilen. Die beperkingen
      kunnen in van alles zitten, bijvoorbeeld in het materiaal, kleur of
      formaat. Als je bij de toelating aan aankomende studenten vraagt iets over
      zichzelf te vertellen in beeld en zegt ‘hier ligt het materiaal’, krijg je
      vaak een heel saai en niet-creatief eindresultaat want alles is mogelijk.
      Als je aan aankomende studenten vraagt iets over zichzelf te vertellen in
      beeld en zegt ‘hier ligt het materiaal en het mag niet groter zijn dan 10
      x 10 cm’, dan gebeurt er iets. Wat ik in mijn lessen ook vaak doe, is dat
      ik met hun eindresultaat iets extreems doe. Ik laat ze een hele kleine
      tekening bijvoorbeeld enorm uitvergroten of zet het werk in een andere
      context, bijvoorbeeld in een mini-museumpje, of ik plaats ze in een andere
      omgeving zoals jij doet met de elementen in je Levenslandschappen.”
    </p>

    <p class="boldintro">
      Dan zien ze dat hun tekening niet alleen maar een krabbeltje hoeft te zijn
      en kunnen ze het meer op waarde schatten. Het gaat erom dat ze die
      tekening eer aandoen. Dat is eigenlijk ook wat jij wil, toch? Dat ze hun
      herinneringen eer aandoen, een plaats geven, door ze te tekenen. Eigenlijk
      zit het hem vooral in de aandacht die iets krijgt. Een tekening ergens
      onderin een wiskundeschriftje is toch maar een tekening. Maar als de
      tekening met aandacht, of vanuit noodzaak gemaakt is, tijdens een workshop
      of spel en als het vervolgens ook van anderen de aandacht krijgt die het
      verdient, zullen mensen er zelf veel eerder trots op zijn. In de
      allereerste vormen van tekenonderwijs leerde je bijvoorbeeld stapsgewijs
      een eend te tekenen. Als je alle stappen volgt, heb je gegarandeerd een
      succesbeleving maar dan heb je uiteindelijk bar weinig ontdekt of
      onderzocht en dat is dus vanuit een heel ander perspectief en een andere
      gedachte dan waarom jij mensen wil laten tekenen.In deze tijd zie je dat
      je aandacht op allerlei manieren getrokken wordt door je mobiele telefoon,
      iPad, Instagram en Snapchat. Maar getrokken aandacht is geen gerichte
      aandacht. Gerichte aandacht is actief en autonoom. Leren om je te focussen
      is belangrijker dan ooit geworden en tekenen zorgt er juist voor dat je je
      aandacht ergens actief op richt waardoor je iets zichtbaar maakt, ook voor
      jezelf. Ik merk dat leerlingen bij ons op het Fons Vitae (we hebben
      inmiddels een iPadschool) steeds vaker vragen of ze ook papier mogen
      gebruiken. We zijn inmiddels zo gewend dat het apparaat overal tussen zit
      maar mensen missen het tastbare, het persoonlijke handschrift. In iMessage
      kan je sinds kort ook iets met de hand schrijven. Dat is niet voor niets
      en het is heel wat anders dan een voorgebakken smiley. Mensen hebben juist
      weer behoefte aan het persoonlijke van een handgeschreven boodschap.
    </p>

    <p>
      Ik vertel Oskar over hoe ik in mijn tekenmethodes elementen uit de
      narratieve psychologie gebruik en vraag hem wat hij daarvan vindt:
    </p>

    <p class="boldintro">
      “Ik heb zelf op de vrije school gezeten, mijn vrouw geeft daar nu les en
      mijn zus heeft creatieve therapie gestudeerd. Ik geloof absoluut in de
      waarde van dat type onderwijs waar ook gekeken wordt naar de ‘innerlijke
      wereld’ achter het werk, alleen vind ik dat je wel moet uitkijken met te
      snel psychologiseren. Toen ik nog op school zat, moest je bijvoorbeeld een
      boom tekenen. Als je een boom zonder bladeren tekende, was je niet
      gelukkig en een boom zonder wortels betekende dat je je niet gehecht had.
      Het zou af en toe best kunnen kloppen maar het is geen absolute waarheid.
      Eigenlijk moet je zo’n tekening aandacht geven zonder dat er een conclusie
      aanhangt. Het gaat puur om iets zichtbaar maken maar alleen als
      constatering en niet als oordeel. Het is natuurlijk wel heel interessant
      om naar te kijken. Welke kleuren kiest iemand? Hoe vertaalt de een
      bepaalde muziek naar beeld en hoe doet de ander dat? Die eigenheid kan
      beeld wel in zich hebben en blootleggen. Als twee mensen die nooit
      tekenen, gaan tekenen, stellen ze zich kwetsbaar op en dat zorgt al meteen
      voor betere communicatie. De setting is dan veel meer op uitwisseling
      gericht. Dat snapt Studio Moniker met hun Conditional design ook heel
      goed: je zit met zijn drieëen om een tafel heen en om de beurt moet je
      iets tekenen. Dan ontstaat er vanzelf iets tussen die mensen.”
    </p>

    <p>
      Oskar ziet een duidelijk verschil tussen gerichte aandacht en getrokken
      aandacht. Ook is hij ervan overtuigd dat uiteindelijk iedereen plezier zou
      kunnen hebben in het tekenen en wordt duidelijk waarom (spel)regels ervoor
      zorgen dat mensen beperkingen ervaren waardoor ze gemakkelijker tot
      tekenen overgaan.
    </p>
  </section>
