    <h2>Na Fase 3: eindvisualisaties?</h2>

    <p class="bolditalic">
      We all know that drawing can be a tool for research, reflection, analysis,
      investigation and experimentation. It can describe, record, map, plot,
      scrutinize, and propose. All of these are transferable skills and drawing
      as a process can support and enhance learning and understanding in many
      subjects and disciplines. UIT THE BIGGER PICTURE OF DRAWING: A NEW
      CURRICULUM, A NEW PEDAGOGY
    </p>

    <p>
      Mits iedereen meedoet en alles goed gaat, is het doel van verbinding en
      reflectie na een aantal rondes bereikt. Als er ook iets met de
      eindresultaten wordt gedaan, kan dat niet alleen een extra reflectiemoment
      opleveren maar er ook voor zorgen dat studenten beter onthouden wat ze
      besproken hebben, en het eventueel waar nodig ook toepassen. Het hangt erg
      van het thema af of deze ‘eindopdracht’ zinvol is. Zo heb ik een familie
      een gezamenlijk kunstwerk laten maken aan de hand van de methode. In de
      therapeutische setting, waar de methode als ijsbreker bij een
      groepstherapie werd gebruikt, bleek alleen het spelen van het spel
      voldoende. De studenten van het hbo heb ik een aantal keren wel gevraagd
      om een eindopdracht te maken. In het begin heb ik ze daar zelf invulling
      aan laten geven door ze de zelfgemaakte tekeningen uit te laten knippen en
      deze op de een of andere manier te laten structureren (en plakken) op hun
      papier. Ze mochten ook nieuwe tekeningen maken of voornamelijk tekst
      gebruiken. Deze ‘opdracht’ bleek te vrijblijvend en de meeste studenten
      wisten er dan ook geen raad mee. Toen ik een aantal duidelijke ‘regels’
      gaf voor het maken van zo’n eindvisualisatie konden ze er veel beter mee
      uit de voeten.
    </p>

