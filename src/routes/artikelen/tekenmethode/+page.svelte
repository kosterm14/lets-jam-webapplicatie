      helpt mensen om specifieker over bepaalde onderwerpen na te denken en het
      kan ook gesprekken op gang brengen. De spelelementen en setting zorgen
      ervoor dat de drempel om te tekenen niet zo groot is en dat het schetsen
      voelt als een gemeenschappelijke bezigheid. Dat was het geval bij mensen
      in een therapeutische setting, bij families en vriendengroepen. Het was
      niet anders bij de studenten in het hbo. Door de methode zo vaak met
      eenzelfde doelgroep te testen is me wel duidelijker geworden hoe de
      verschillende onderdelen zich tot elkaar verhouden.
    </p>

