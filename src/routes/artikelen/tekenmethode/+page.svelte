    <h2>Resultaten</h2>

    <p>
      Mijn methode in het hbo is een breed inzetbare ‘toolkit’ voor docenten
      geworden. De docent kan zelf bepalen op welke fases van de methode hij of
      zij de nadruk wil leggen. Ook is er ruimte om zelf thema’s te verzinnen
      waarover getekend wordt en om te bepalen wat er met de eindresultaten
      gedaan wordt. Zo was er een docent die het nuttig vond om studenten op
      deze manier op hun samenwerking te laten reflecteren:
    </p>

    <p>
      Als ik studenten een vragenlijst geef over hoe hun samenwerking verloopt,
      geven ze al snel sociaal wenselijke antwoorden. Ze op deze manier laten
      tekenen en praten maakt dat hun gedachten en antwoorden natuurlijker zijn.
    </p>

    <p>
      Een andere docent gebruikte het als opstapje naar een grotere opdracht die
      ook te maken had met het persoonlijk leven en de omgeving van studenten.
      De mate waarin verdieping in een gesprek kan plaatsvinden hangt erg af van
      de (motivatie van) de student en of de docent een hele klas onder zijn
      hoede heeft of slechts een klein groepje. In dat laatste geval kan hij of
      zij ook fungeren als moderator en mogelijk zelf verdiepende vragen stellen
      en het gesprek een kant op sturen.
    </p>

    <h3>Nut?</h3>

    <p class="bolditalic">
