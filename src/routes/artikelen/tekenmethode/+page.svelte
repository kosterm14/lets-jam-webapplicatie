    <h2>
      Fase 3: Delen: vertellen over de tekeningen, luisteren naar elkaars
      verhalen en vragen stellen.
    </h2>

    <p>
      In de derde fase vertelt men over de tekeningen en stelt men elkaar
      vragen, eventueel aan de hand van een vraagkaart. Fase drie wordt weer
      gemeenschappelijk gedaan.
    </p>

    <p class="bolditalic">
      We hebben een groot gebrek aan creativiteit, verbeeldingskracht en
      kritisch denkvermogen. Het stellen van scherpe vragen helpt ons dat te
      ontwikkelen en de ingewikkelde gesprekken beter, genuanceerder, rijker en
      vrijer te voeren. E. WISS, SOCRATES OP SNEAKERS
    </p>

    <p>
      De bedoeling van mijn methode is dat er verbinding ontstaat. Een
      (vraag)gesprek kan daarvoor zorgen. Dat blijkt voor studenten niet altijd
      makkelijk. Ze legden om de beurt uit wat ze getekend hadden, maar daar
      bleef het dan bij. Het gebeurde nog te weinig dat ze elkaar vervolgens
      ondervroegen. Voor de docenten was het ook lastig om vragen te stellen
      naar aanleiding van de tekeningen, simpelweg omdat ze een aantal groepjes
      tegelijk onder hun hoede hadden en er geen tijd en ruimte voor was.
    </p>

    <h2>Wat moet ik dan vragen?</h2>

    <p>
      De studenten wisten dus vaak niet wat ze elkaar moesten vragen. Ik bedacht
      onderstaand schema om dat probleem mee te ondervangen. Dit begrip bleek
      ‘laddering’ te heten en komt uit de marketing. Er doen zich telkens twee
      situaties voor. Deelnemers tekenen en vertellen soms iets heel algemeens.
      In dat geval is het goed om naar een voorbeeld te vragen. Andersom komt
      ook veel voor. In dat geval is het interessant als een deelnemer die
      specifieke situatie kan veralgemeniseren.
    </p>
    
    <img src="/Algemeen-Specifiek-1-768x536.jpg" alt="Algemeen-Specifiek">

    <p>
      Ik heb dit schema bij de laatste twee tests aan docenten gegeven en bij
      studenten op tafel gelegd, maar voor de studenten was het veel te complex
      om zich ter plekke eigen te maken en de docenten hadden er te weinig tijd
      en ruimte voor. Het is overigens wel mogelijk als een docent de moderator
      is van een groepje en het gesprek kan leiden.
    </p>

    <img src="/Schema_Socrates_Sneakers.png" alt="Schema_Socrates_Sneakers">

    <p>
      Ik kwam een soortgelijk schema ook tegen in het boek ‘Socrates op
      Sneakers’ over het nut van Socratische gespreksvoering. Juist ‘de concrete
      werkelijkheid’ wordt vaak getekend en zorgt voor anekdotes waar je als
      spelpartner goed in mee kan gaan. Het is niet altijd nodig om door te
      vragen om iemand beter te leren kennen. Zo’n anekdote kan vaak al heel
      veel prijsgeven. Andersom, dus van abstract naar concreet, zorgt vaak wel
      voor een leuker en verbindender gesprek.
    </p>

    <h2>Vraagkaarten</h2>

    <p>
      Een van mijn collega’s kwam met een hele goede oplossing voor het probleem
      dat studenten niet doorvragen. Hij stelde voor om een extra regel in te
      bouwen. De regel luidt als volgt: als iemand zijn/haar verhaal n.a.v een
      tekening verteld heeft, moet een van de andere studenten hem/haar een
      vraag stellen aan de hand van de vraagkaarten die in het middel van de
      tafel liggen. Dit simpele idee leidde er toe dat er in veel meer gevallen
      daadwerkelijk een gesprek werd gevoerd. De regel is niet voor elke
      doelgroep noodzakelijk maar kan toegepast worden wanneer deelnemers moeite
      hebben met het voeren van een gesprek en er geen moderator aanwezig is om
      door te vragen over de gemaakte tekeningen.
    </p>

    <img src="/Docent.webp" alt="Docent">

    <p>
      De docent kan niet in zijn eentje zeven groepjes van vier begeleiden. De
      nieuwe regel zorgt ervoor dat de studenten elkaar zelf vragen stellen aan
      de hand van de gemaakte tekeningen
    </p>

    <h2>Ongemakkelijk</h2>

    <p class="bolditalic">
      We zijn bang om wezenlijke, spannende vragen te stellen die mogelijk
      leiden tot ongemak, maar ook kunnen leiden tot verbinding, omdat dat
      allemaal gepaard gaat met kwetsbaarheid. We gaan er te snel van uit dat
      een ander niet op een bepaalde vraag zit te wachten. E. WISS, SOCRATES OP
      SNEAKERS
    </p>

    <p>
      Een paar studenten gaven aan sommige dingen niet te willen delen en het
      ook lastig te vinden om persoonlijke vragen te stellen. Ik ging ervan uit
      dat studenten als vanzelf hun eigen grenzen aan zouden geven, geen dingen
      zouden prijsgeven die ze liever voor zichzelf hielden en zich bij dit
      onderdeel dus ook niet ongemakkelijk zouden voelen. Dat bleek niet altijd
      het geval en eventueel ongemak is dus iets waar rekening mee gehouden moet
      houden. Dit kan door tijdens de introductie duidelijk te maken dat niemand
      verplicht is om persoonlijke onderwerpen te delen of om anderen
      persoonlijke vragen te stellen. Er zit wat mij betreft wel een grote
      meerwaarde in als mensen het wel doen, ook als ze met elkaar verbonden
      zijn door werk of studie, maar het moet niet als een verplichting voelen.
    </p>

    <h3>Thema’s</h3>

    <p>
      Uit mijn eerdere onderzoek wist ik dat de methode zich goed leent voor
      persoonlijke thema’s. Door het maken van een tekening blijft men minder
      aan de oppervlakte dan wanneer iets alleen in gesproken of geschreven taal
      wordt uitgedrukt. Daarnaast helpen de vraagkaarten om het thema
      specifieker te maken (dit noem ik in mijn eindpublicatie ‘ankerpunten’).
      Als het thema Liefde is, is het gemakkelijk (en algemeen) een hart te
      tekenen. Als het thema Liefde en Waar is, zul je met een specifiekere
      tekening moeten komen. In het hbo heb ik voor het eerst andere thema’s
      uitgeprobeerd, namelijk De rol van een ontwerper en Samenwerken. Ik kan
      inmiddels wel vaststellen dat het lastiger wordt om te tekenen naarmate
      het thema abstracter is en verder af staat van de personen die meedoen met
      het spel. Dit wil niet zeggen dat het niet mogelijk is. Zo heb ik ontdekt
      dat het thema Samenwerken, waar toch een aantal abstractere themawoorden
      onder vallen, wel werkt als iedereen gemotiveerd is om mee te doen. Mijn
      aanname is dat de drie fases in mijn methode werken ongeacht welk thema er
      ingezet wordt, maar dat het afhangt van de doelgroep en motivatie of er
      ook echt interessante gesprekken ontstaan die kunnen verdiepen en
      verbinden.
    </p>

